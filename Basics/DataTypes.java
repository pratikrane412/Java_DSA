public class DataTypes {
    public static void main(String[] args) {
        int a = 12;
        System.out.println(a);

        String s = "Pratik";
        System.out.println(s);

        char ch = 'S';
        System.out.println(ch);

        float f = 1.2f;
        System.out.println(f);

        double db = 1.23;
        System.out.println(db);

        boolean bl = true;
        System.out.println(bl);
    }
}
